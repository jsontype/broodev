// 브라우저 언어 자동 감지 → 지원 언어 매칭 (저장된 선택이 없을 때의 첫 진입 초기값).
// navigator.languages 우선순위대로 base 코드를 보고 첫 매칭을 채택.
// 중국어는 번체(zh-TW/HK/Hant)→zh-Hant, 그 외→간체 zh. 매칭 실패 시 영어로 폴백.
export function detectLang() {
  try {
    const supported = LANGS.map((l) => l.code)
    const cands = navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language]
    for (const raw of cands) {
      const lc = String(raw || '').toLowerCase()
      const base = lc.split('-')[0]
      // 중국어: 번체(대만/홍콩/마카오/Hant) → zh-Hant, 그 외 → 간체 zh
      if (base === 'zh') {
        if (/hant|tw|hk|mo/.test(lc) && supported.includes('zh-Hant')) return 'zh-Hant'
        if (supported.includes('zh')) return 'zh'
      }
      if (supported.includes(base)) return base
    }
  } catch {
    /* navigator 없음(SSR 등) → 폴백 */
  }
  return 'en'
}

